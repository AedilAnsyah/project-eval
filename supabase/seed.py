import openpyxl
import json
import uuid
import os

def generate_dob(no_urut):
    # Generates a valid date of birth. Max no_urut is 53.
    # To keep days valid for any month, we use 28 days per month.
    # month: 1 + (no_urut - 1) // 28
    # day: 1 + (no_urut - 1) % 28
    month = 1 + (no_urut - 1) // 28
    day = 1 + (no_urut - 1) % 28
    return f"2004-{month:02d}-{day:02d}"

def infer_role_and_dept(jabatan):
    j_lower = jabatan.lower()
    
    # Check role
    if "chairman" in j_lower:
        role = "admin"
    elif "core of" in j_lower:
        role = "koor"
    else:
        role = "staff"
        
    # Check department
    if any(k in j_lower for k in ["chairman", "secretary", "treasure"]):
      dept = "Executive Board"
    elif "human capital" in j_lower:
        dept = "Human Capital & Character Building"
    elif "talent development" in j_lower:
        dept = "Talent Development & Innovation"
    elif "humanity impact" in j_lower:
        dept = "Humanity Impact & Development"
    elif "finance" in j_lower:
        dept = "Finance & Enterprise Development"
    elif "creative content" in j_lower:
        dept = "Creative Content & Outreach"
    elif "external relations" in j_lower:
        dept = "External Relations & Advocacy"
    else:
        dept = "Lainnya"
        
    return role, dept

def main():
    excel_path = r"C:\Users\Acer\Downloads\PESAN ATAU KESAN.xlsx"
    if not os.path.exists(excel_path):
        print(f"Error: Excel file not found at {excel_path}")
        return

    wb = openpyxl.load_workbook(excel_path)
    sheet = wb.active

    members = []
    
    # Headers are on row 2, data starts from row 3 to 55 (53 members)
    for r in range(3, sheet.max_row + 1):
        vals = [cell.value for cell in sheet[r]]
        # Column B is NO (index 1), Column C is NAMA (index 2), Column D is JABATAN (index 3)
        no_val = vals[1]
        nama_val = vals[2]
        jab_val = vals[3]
        
        if no_val is None or nama_val is None:
            continue
            
        no_urut = int(no_val)
        nama = str(nama_val).strip()
        jabatan = str(jab_val).strip()
        
        role, dept = infer_role_and_dept(jabatan)
        
        # Generate credentials
        nim = f"1012200{no_urut}" if no_urut < 10 else f"101220{no_urut}"
        dob = generate_dob(no_urut)
        member_id = str(uuid.uuid4())
        
        # Generate avatar using Dicebear Fun Emoji
        avatar_url = f"https://api.dicebear.com/7.x/fun-emoji/svg?seed={nama.replace(' ', '')}"
        
        # Mock evaluation messages
        # These are template messages that can be read in the love letter UI
        pesan_fatir = f"Halo {nama}, terima kasih banyak atas kontribusimu sebagai {jabatan} di periode kepengurusan ini. Kerjamu sangat luar biasa dan sangat membantu kelancaran program kerja HMIF. Tetap semangat dan semoga sukses di langkah berikutnya!"
        pesan_aedil = f"Hai {nama}! Salut dengan dedikasi dan energi positif yang kamu bawa. Sebagai {jabatan}, kamu sudah memberikan yang terbaik. Semoga pengalaman di HMIF ini bermanfaat untuk masa depanmu ya!"
        pesan_koor = f"Halo {nama}, sebagai rekan satu tim, aku bangga sekali bisa bekerja bersamamu di departemen {dept}. Kerja kerasmu sangat menginspirasi. Terima kasih atas segala bantuan dan kebersamaan kita!"
        
        members.append({
            "id": member_id,
            "no_urut": no_urut,
            "nama": nama,
            "nim": nim,
            "tanggal_lahir": dob,
            "role": role,
            "departemen": dept,
            "foto_url": avatar_url,
            "pesan_fatir": pesan_fatir,
            "pesan_aedil": pesan_aedil,
            "pesan_koor": pesan_koor,
            "jabatan": jabatan
        })
        
    print(f"Parsed {len(members)} members successfully.")
    
    # 1. Output src/lib/members_data.js
    os.makedirs(r"c:\Users\Acer\Documents\project-eval\src\lib", exist_ok=True)
    js_content = f"// File auto-generated by seed.py. Contains mock data for 53 members.\n\nexport const members = {json.dumps(members, indent=2)};\n"
    with open(r"c:\Users\Acer\Documents\project-eval\src\lib\members_data.js", "w", encoding="utf-8") as f:
        f.write(js_content)
    print("Created members_data.js")
    
    # 2. Output credentials.md in the workspace root
    cred_content = "# Credentials Akun Anggota HMIF untuk Login\n\n"
    cred_content += "Gunakan NIM dan Tanggal Lahir di bawah ini untuk masuk ke dalam sistem evaluasi.\n\n"
    cred_content += "| No | Nama | Jabatan | Departemen | NIM | Tanggal Lahir | Role |\n"
    cred_content += "|---|---|---|---|---|---|---|\n"
    for m in members:
        cred_content += f"| {m['no_urut']} | {m['nama']} | {m['jabatan']} | {m['departemen']} | `{m['nim']}` | `{m['tanggal_lahir']}` | `{m['role']}` |\n"
        
    with open(r"c:\Users\Acer\Documents\project-eval\credentials.md", "w", encoding="utf-8") as f:
        f.write(cred_content)
    print("Created credentials.md")
    
    # 3. Output supabase/seed_data.sql
    os.makedirs(r"c:\Users\Acer\Documents\project-eval\supabase", exist_ok=True)
    sql_content = "-- SQL Seed: Populate members (anggota)\n\n"
    for m in members:
        # Escape single quotes in strings
        esc_nama = m['nama'].replace("'", "''")
        esc_dept = m['departemen'].replace("'", "''")
        esc_foto = m['foto_url'].replace("'", "''")
        esc_fatir = m['pesan_fatir'].replace("'", "''")
        esc_aedil = m['pesan_aedil'].replace("'", "''")
        esc_koor = m['pesan_koor'].replace("'", "''")
        
        sql_content += f"INSERT INTO anggota (id, no_urut, nama, nim, tanggal_lahir, role, departemen, foto_url, pesan_fatir, pesan_aedil, pesan_koor) \n"
        sql_content += f"VALUES ('{m['id']}', {m['no_urut']}, '{esc_nama}', '{m['nim']}', '{m['tanggal_lahir']}', '{m['role']}', '{esc_dept}', '{esc_foto}', '{esc_fatir}', '{esc_aedil}', '{esc_koor}')\n"
        sql_content += f"ON CONFLICT (nim) DO UPDATE SET \n"
        sql_content += f"  nama = EXCLUDED.nama, role = EXCLUDED.role, departemen = EXCLUDED.departemen, foto_url = EXCLUDED.foto_url;\n\n"
        
    with open(r"c:\Users\Acer\Documents\project-eval\supabase\seed_data.sql", "w", encoding="utf-8") as f:
        f.write(sql_content)
    print("Created seed_data.sql")

if __name__ == "__main__":
    main()
